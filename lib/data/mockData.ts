export const generateMockSlots = (experienceId: string): TimeSlot[] => {
    const slots: TimeSlot[] = [];
    const today = new Date();

    for (let day = 1; day <= 14; day++) {
        const date = new Date(today);
        date.setDate(today.getDate() + day);
        const dateString = date.toISOString().split('T')[0];

        // Morning slot
        slots.push({
            id: `${experienceId}-${dateString}-morning`,
            experienceId,
            date: dateString,
            startTime: '08:00',
            endTime: '11:00',
            availableSpots: Math.floor(Math.random() * 10) + 1,
            totalSpots: 12,
            price: parseInt(mockExperiences.find(e => e.id === experienceId)?.price?.replace('₹', '') || '100'),
            status: Math.random() > 0.2 ? 'available' : 'sold-out',
        });

        // Afternoon slot
        slots.push({
            id: `${experienceId}-${dateString}-afternoon`,
            experienceId,
            date: dateString,
            startTime: '14:00',
            endTime: '17:00',
            availableSpots: Math.floor(Math.random() * 10) + 1,
            totalSpots: 12,
            price: parseInt(mockExperiences.find(e => e.id === experienceId)?.price?.replace('₹', '') || '100'),
            status: Math.random() > 0.3 ? 'available' : 'sold-out',
        });
    }

    return slots;
};

